void infra_devapc_init(void)
{
	const struct devapc_desc *dapc_desc;
	uint32_t dapc_num;
	uint32_t i;

	dapc_desc = &dapc_list[0];
	dapc_num = ARRAY_SIZE(dapc_list);

	for (i = 0; i < dapc_num; i++) {
		devapc_writel(BIT(31), AO_APC_CON(dapc_desc[i].ao_base));
		mmio_setbits_32(AO_APC_CON(dapc_desc[i].ao_base), APC_CON_SEN);

		if (dapc_desc[i].pd_base)
			devapc_writel(BIT(31), AO_APC_CON(dapc_desc[i].pd_base));

		set_ao_master_attr(&dapc_desc[i]);
		set_ao_domain_remap(&dapc_desc[i]);

		set_ao_system_apc(&dapc_desc[i]);
		set_ao_apc_lock(&dapc_desc[i]);
	}

	for (i = 0; i < dapc_num; i++) {
		dump_ao_master_attr(&dapc_desc[i]);
		dump_ao_domain_remap(&dapc_desc[i]);

		dump_ao_system_apc(&dapc_desc[i]);
		dump_ao_apc_lock(&dapc_desc[i]);
	}

	INFO("[DEVAPC] %s done\n", __func__);
}
