static void set_system_apc(const struct devapc_desc *desc,
			   const struct devapc_ao_system *sys, uint32_t dom_num)
{
	if (dom_num == 16) {
		set_module_apc_16(desc, sys, sys->modules, sys->mod_num);
	} else if (dom_num == 4) {
		set_module_apc_4(desc, sys, sys->modules, sys->mod_num);
	} else {
		ERROR("[DEVAPC] Invalid domain number\n");
		panic();
	}
}

static void set_ao_system_apc(const struct devapc_desc *desc)
{
	uint32_t i;

	for (i = 0; i < desc->sys_num; i++)
		set_system_apc(desc, &desc->sys[i], desc->sys[i].dom_num);
}

static void set_ao_system_apc_lock(const struct devapc_desc *desc,
				   const struct devapc_ao_system *sys,
				   uint32_t mod_num, uint32_t dom_num)
{
	uint32_t i;
	uint32_t reg;
	uint32_t reg_num;
	uint32_t last_mod_num;

	reg_num = (mod_num - 1) / 32;
	last_mod_num = mod_num % 32;
	for (i = 0; i <= reg_num; i++) {
		reg = AO_SYS0_APC_LOCK(desc->ao_base) + sys->id * 0x1000 + i * 4;
		if (i == reg_num)
			devapc_writel(GENMASK(last_mod_num, 0), reg);
		else
			devapc_writel(GENMASK(31, 0), reg);
	}
}

static void set_ao_apc_lock(const struct devapc_desc *desc)
{
	uint32_t i;

	devapc_writel(0x30a, AO_APC_CON1(desc->ao_base));

	for (i = 0; i < desc->sys_num; i++) {
		set_ao_system_apc_lock(desc, &desc->sys[i], desc->sys[i].mod_num,
				       desc->sys[i].dom_num);
	}
}

