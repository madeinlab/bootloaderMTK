static void set_ao_master_domain(const struct devapc_desc *desc,
				 int master, uint8_t domain)
{
	uint32_t mas_dom_reg_index;
	uint32_t mas_dom_set_index;
	uintptr_t reg;
	uint32_t clr_bit;
	uint32_t set_bit;

	if (master >= 0) {
		mas_dom_reg_index = master / MAS_NO_IN_1_MAS_DOM;
		mas_dom_set_index = master % MAS_NO_IN_1_MAS_DOM;

		clr_bit = 0xfU << (mas_dom_set_index * 8);
		set_bit = (uint32_t)domain << (mas_dom_set_index * 8);

		reg = AO_MAS_DOM(desc->ao_base) + mas_dom_reg_index * 4;
		mmio_clrsetbits_32(reg, clr_bit, set_bit);
	}
}

static void set_ao_master_secure(const struct devapc_desc *desc,
				 int master, uint8_t secure)
{
	uint32_t mas_sec_reg_index;
	uint32_t mas_sec_set_index;
	uintptr_t reg;
	uint32_t clr_bit;
	uint32_t set_bit;

	if (master >= 0) {
		mas_sec_reg_index = master / 32;
		mas_sec_set_index = master % 32;

		clr_bit = 1U << mas_sec_set_index;
		set_bit = (uint32_t)secure << mas_sec_set_index;

		reg = AO_MAS_SEC(desc->ao_base) + mas_sec_reg_index * 4;
		mmio_clrsetbits_32(reg, clr_bit, set_bit);
	}
}

static void set_ao_master_attr(const struct devapc_desc *desc)
{
	uint32_t i;

	for (i = 0; i < desc->mas_num; i++) {
		set_ao_master_domain(desc, desc->mas[i].dom_idx, desc->mas[i].dom);
		set_ao_master_secure(desc, desc->mas[i].sec_idx, desc->mas[i].sec);
	}
}

static void set_domain_remap(const struct devapc_desc *desc, uint8_t remap_dom_bit,
			     uint8_t domain, uint8_t remap_dom)
{
	uint32_t dom_remap_reg_index;
	uint32_t dom_remap_set_index;
	uintptr_t reg;
	uint32_t clr_bit;
	uint32_t set_bit;

	dom_remap_reg_index = domain * remap_dom_bit / 32;
	dom_remap_set_index = domain * remap_dom_bit % 32;

	clr_bit = GENMASK(remap_dom_bit - 1, 0) << dom_remap_set_index;
	set_bit = (uint32_t)remap_dom << dom_remap_set_index;

	reg = AO_DOM_REMAP(desc->ao_base) + dom_remap_reg_index * 4;
	mmio_clrsetbits_32(reg, clr_bit, set_bit);
}

static void set_ao_domain_remap(const struct devapc_desc *desc)
{
	uint32_t i;

	if (desc->remap_dom_bit) {
		for (i = 0; i < (1 << desc->dom_bit); i++) {
			set_domain_remap(desc, desc->remap_dom_bit,
					 desc->remap_table[i].dom,
					 desc->remap_table[i].remap_dom);
		}
	}
}

static void set_module_apc(const struct devapc_desc *desc,
			   const struct devapc_ao_system *sys,
			   uint32_t module, uint8_t domain, int8_t perm)
{
	uint32_t apc_reg_index;
	uint32_t apc_set_index;
	uintptr_t base, reg;
	uint32_t clr_bit;
	uint32_t set_bit;

	apc_reg_index = module / MOD_NO_IN_1_DEVAPC;
	apc_set_index = module % MOD_NO_IN_1_DEVAPC;

	clr_bit = 0x3U << (apc_set_index * 2);
	set_bit = (uint32_t)perm << (apc_set_index * 2);

	base = AO_SYS0_D0_APC(desc->ao_base) + sys->id * 0x1000;
	reg = base + domain * 0x40 + apc_reg_index * 4;

	mmio_clrsetbits_32(reg, clr_bit, set_bit);
}

